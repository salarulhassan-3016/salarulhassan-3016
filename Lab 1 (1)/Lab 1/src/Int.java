public class Int {
    private int value;

    // Initialize to zero
    public void initToZero() {
        this.value = 0;
    }

    // Initialize to an integer value
    public void initToValue(int value) {
        this.value = value;
    }

    // Display the value
    public void display() {
        System.out.println("Value: " + this.value);
    }
}
